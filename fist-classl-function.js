const add = function(a, b) {
    return a + b;
}

const subtract = function(a, b) {
    return a - b
}

const multply = function(a, b) {
    return a * b
}

const divide = function(a, b) {
    return a / b
}

console.log(add(10, 20))
console.log(subtract(10, 20))
console.log(multply(10, 20))
console.log(divide(10, 20))